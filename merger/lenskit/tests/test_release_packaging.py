def test_symlink_target_must_remain_inside_archive(tmp_path: Path) -> None:
    assert safe_symlink_target("nested/link", "../regular.txt")
    assert not safe_symlink_target("link", "../outside.txt")
    assert not safe_symlink_target("nested/link", "/etc/passwd")

    repo = _repo(tmp_path)
    os.symlink("../../outside.txt", repo / "nested-escape")
    _git(repo, "add", "nested-escape")
    _git(repo, "commit", "-qm", "unsafe symlink")
    with pytest.raises(ValueError, match="unsafe symlink target"):
        build_release_candidate(repo, tmp_path / "candidate")


